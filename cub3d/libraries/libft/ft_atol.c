#include "libft.h"

long	ft_atol(const char *str)
{
	int		sign;
	long	result;

	result = 0;
	sign = 1;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (ft_isnumeric(*str))
	{
		result = result * 10 + (*str - '0');
		str++;
	}
	return (result * sign);
}
