int		**debug_create_map(int map[mapWidth][mapHeight])
{
	int		**result;
	int		i;
	int		j;

	result = malloc(sizeof(int *) * mapHeight);
	i = 0;
	while (i < mapHeight)
	{
		result[i] = malloc(sizeof(int) * mapWidth);
		j = 0;
		while (j < mapWidth)
		{
			result[i][j] = map[i][j];
			j++;
		}
		i++;
	}
	return (result);
}

